public class Pair{


    public String first;

    public String getFirst() {
        return first;
    }

    public void setFirst(String first) {
        this.first = first;
    }

    public String second;

    public String getSecond() {
        return second;
    }

    public void setSecond(String second) {
        this.second = second;
    }


    public void Pair(String first, String second){
        this.first = first;
        this.second = second;
    }
}
